public class NumSwap {
    public static void main(String[] args) { 
      int a, b, temp;
      a = 5;
      b = 10;
      System.out.println("Before swapping : a, b = "+a+", "+ + b);
      temp = a;
      a = b;
      b = temp;   
     System.out.println("After swapping : a, b = "+a+", "+ + b);
    }
}